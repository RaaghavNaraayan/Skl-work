'''This is a simple calculator which takes in 2 values and gives the Sum, Diff, Mul, Div'''
a=int(input("Enter 1st number ="))
b=int(input("Enter 2nd number ="))
Sum=a+b
print("Sum of the numbers =",Sum)
Diff=a-b
print("Diff of the numbers =",Diff)
Mul=a*b
print("Mul of the numbers =",Mul)
Div=a/b
print("Div of the numbers =)",Div)
